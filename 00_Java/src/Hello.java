
public class Hello {

	public static void main(String[] args) {
		// System.out은 출력을 나타냄
		// print() 괄호 안에 전달된 값을 출력
		// ; 은 코드에서 하나의 명령이 끝날 때마다 작성
		System.out.print("안녕하세요.");
		System.out.print("반갑습니다.");
		// println()은 문장이 끝난 다음에 enter가 진행
		System.out.println("환영합니다.");
		// 줄맞추기 tab
		System.out.println("어서오세요. 점심시간");
		
		System.out.println("1");
		System.out.println("23456789");
		System.out.println("01234");
		System.out.println(1);
		// 0을 앞에 넣으면 단순히 1로 출력이 됨 
		// 0을 앞에서 출력하고 싶으면 "" 를 이용해서 출력함
		System.out.println(01);
		System.out.println(true); //true = 진실 , 사실
		// 단축키 !   sysout 후 control + space 하면 자동으로
		// System.out.println(); 이 완성이 됨
	}

}
